def get_bank_name(code: str) -> str:
    """Returns the Thai bank name for a given 3-digit bank code."""
    if not code:
        return "ไม่ระบุธนาคาร"
    return BANK_MAPPING.get(code, f"ธนาคารรหัส {code}")
